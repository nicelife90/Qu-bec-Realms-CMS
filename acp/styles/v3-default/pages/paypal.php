          Below you will find a short easy to follow guide on how to setup <b>Paypal IPN</b> so your donation system will be completely operational.
          <br/><br/>
          <b>Step 1.</b> Log on to your Paypal account at <a href="http://www.paypal.com/">http://www.paypal.com/</a>
          <br/>
          <b>Step 2.</b> Go to this url <a href="http://www.paypal.com/us/cgi-bin/webscr?cmd=_profile-ipn-notify">http://www.paypal.com/us/cgi-bin/webscr?cmd=_profile-ipn-notify</a> to setup Paypal IPN.
          <br/>
          <b>Step 3.</b> Click "Edit Settings".
          <br/>
          <b>Step 4.</b> Fill out the Notification Url like so : <b>http://YourSite.com/core/inc/ipn/ipn.php</b> and click "Receive IPN messages (Enabled)"
          <br/>
          <b>Step 5.</b> Click Save.
          <br/><br/>
          After all of this, you are ready to receive donations from the donation system, so good luck with your server and enjoy!
